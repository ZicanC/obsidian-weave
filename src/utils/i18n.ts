export const trArray = derived(currentLanguage, (_$currentLanguage) =>
	(key: string): string[] => {
		if (!i18n.hasTranslation(key)) return [];
		const text = i18n.t(key);
		if (!text) return [];
		return text
			.split("\n")
			.map((item) => item.trim())
			.filter(Boolean);
	});
